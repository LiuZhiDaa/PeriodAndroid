package com.period.app.bean;

public class TimeFrame {
    private int startYear;
    private int startMonth;
    private int startDay;

    private int endYear;
    private int endtMonth;
    private int endtDay;

    public int getStartYear() {
        return startYear;
    }

    public void setStartYear(int startYear) {
        this.startYear = startYear;
    }

    public int getStartMonth() {
        return startMonth;
    }

    public void setStartMonth(int startMonth) {
        this.startMonth = startMonth;
    }

    public int getStartDay() {
        return startDay;
    }

    public void setStartDay(int startDay) {
        this.startDay = startDay;
    }

    public int getEndYear() {
        return endYear;
    }

    public void setEndYear(int endYear) {
        this.endYear = endYear;
    }

    public int getEndtMonth() {
        return endtMonth;
    }

    public void setEndtMonth(int endtMonth) {
        this.endtMonth = endtMonth;
    }

    public int getEndtDay() {
        return endtDay;
    }

    public void setEndtDay(int endtDay) {
        this.endtDay = endtDay;
    }


}
